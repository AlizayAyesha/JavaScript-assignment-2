// Conversion Formulae

var celsius = 30;
var fahrenheit = (celsius * 9/5) + 32;
console.log(celsius + "°C is " + fahrenheit + "°F");

var fahrenheit2 = 86;
var celsius2 = (fahrenheit2 - 32) * 5/9;
console.log(fahrenheit2 + "°F is " + celsius2 + "°C");

